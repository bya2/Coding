/**
 * @param {string[]} inputs
 */
export const solution = (inputs) => {
  const n = +inputs[0]; // INPUT: N (3의 거듭제곱)
  let s = "";

  const addStar = (i, j, len = n) => {
    if (i % 3 === 1 && j % 3 === 1) {
      s += " ";
    } else if (len === 1) {
      s += "*";
    } else {
      addStar(~~(i / 3), ~~(j / 3), ~~(len / 3));
    }
  };

  for (let i = 0; i < n; ++i) {
    for (let j = 0; j < n; ++j) {
      addStar(i, j);
    }
    s += "\n";
  }

  return s; // OUTPUT
};

